import turtle

pen = turtle.Turtle()
turtle.bgcolor("black")
pen.color("black", "gray")
pen.speed(100)


def goto(x, y):
    pen.penup()
    pen.goto(x, y)
    pen.pendown()


def dr_win(t, x, y):
    goto(x, y)
    pen.color("white", "white")
    pen.begin_fill()
    for i in range(4):
        pen.left(90)
        pen.forward(t)
    pen.end_fill()


